// Creamos una clase llamada Nota que tiene varias propiedades que representan información 
//relacionada con las calificaciones de un estudiante en una asignatura específica

public class Nota
{
    public string Nombre { get; set; }         // Nombre del estudiante
    public string Apellido { get; set; }       // Apellido del estudiante
    public string Asignatura { get; set; }     // Asignatura cursada por el estudiante
    public string Curso { get; set; }          // Curso al que pertenece el estudiante
    public double Nota1 { get; set; }          // Nota del primer parcial
    public double Nota2 { get; set; }          // Nota del segundo parcial
}

